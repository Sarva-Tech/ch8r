@pytest.mark.unit
class TestEmojiDetector:
    def test_is_emoji_only_with_emoji_only(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('👍') is True

    def test_is_emoji_only_with_plus_one(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('+1') is True

    def test_is_emoji_only_with_text(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('Hello world') is False

    def test_is_emoji_only_with_mixed_content(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('Hello 👍') is False

    def test_is_emoji_only_with_empty_string(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('') is False

    def test_is_emoji_only_with_whitespace(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('   ') is False

    def test_is_emoji_only_with_multiple_emojis(self):
        detector = EmojiDetector()
        assert detector.is_emoji_only('👍🎉❤️') is True

    def test_remove_emojis(self):
        detector = EmojiDetector()
        result = detector.remove_emojis('Hello 👍 world')
        assert result == 'Hello world'

    def test_remove_emojis_removes_plus_one(self):
        detector = EmojiDetector()
        result = detector.remove_emojis('Thanks +1')
        assert result == 'Thanks'

    def test_remove_emojis_with_empty_string(self):
        detector = EmojiDetector()
        result = detector.remove_emojis('')
        assert result == ''

    def test_remove_emojis_with_whitespace(self):
        detector = EmojiDetector()
        result = detector.remove_emojis('   ')
        assert result == '   '

    def test_remove_emojis_normalizes_whitespace(self):
        detector = EmojiDetector()
        result = detector.remove_emojis('Hello   👍   world')
        assert result == 'Hello world'


